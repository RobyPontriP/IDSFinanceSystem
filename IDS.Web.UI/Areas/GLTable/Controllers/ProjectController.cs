        public ActionResult Delete(string projectsCodeList)
        {
            if (Session[IDS.Tool.GlobalVariable.SESSION_USER_ID] == null)
                return RedirectToAction("index", "Main", new { area = "" });

            IDS.Web.UI.Models.GroupAccessLevel AccessLevel = IDS.Web.UI.Models.GroupAccessLevel.GetFormGroupAccess(Convert.ToString(Session[IDS.Tool.GlobalVariable.SESSION_USER_GROUP_CODE]), this.ControllerContext.RouteData.Values["controller"].ToString());

            if (AccessLevel.ReadAccess == -1 || AccessLevel.DeleteAccess == 0)
            {
                return Json("NoAccess", JsonRequestBehavior.AllowGet);
            }

            ViewData["Page.Insert"] = AccessLevel.CreateAccess;
            ViewData["Page.Edit"] = AccessLevel.EditAccess;
            ViewData["Page.Delete"] = AccessLevel.DeleteAccess;

            if (string.IsNullOrWhiteSpace(projectsCodeList))
                return Json("Failed", JsonRequestBehavior.AllowGet);

            try
            {
                string[] projectCode = projectsCodeList.Split(new char[] { ',' }, StringSplitOptions.RemoveEmptyEntries);

                if (projectCode.Length > 0)
                {
                    IDS.GLTable.Project project = new IDS.GLTable.Project();
                    project.InsUpDelProject((int)IDS.Tool.PageActivity.Delete, projectCode);
                }

                return Json("Project data has been delete successfull", JsonRequestBehavior.AllowGet);
            }
            catch (Exception ex)
            {
                return Json(ex.Message, JsonRequestBehavior.AllowGet);
            }
        }

